package BasicJava;

public class NumberSystem {
    public static void main(String[] args) {
        int dec = 123;
        int bin = 0b1100101;
        int oct = 0567;
        int hex = 0xA;

        System.out.println(dec);
        System.out.println(bin);
        System.out.println(oct);
        System.out.println(hex);

        /*
        we get output in corresponding decimal format
         */
    }
}
